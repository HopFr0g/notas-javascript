// Math es un objeto que cuenta con los siguientes métodos:

// Math.sqrt(numero); raíz cuadrada del numero
// Math.cbrt(numero); raíz cúbica del numero
// Math.max(numero1, numero2, ..., numeroN) devuelve el mayor número ingresado como parámetro
// Math.min(numero1, numero2, ..., numeroN) devuelve el menor número ingresado como parámetro
// Math.random() devuelve un número random entre 0 y 1.
// Math.round(numero) redondea un número al entero más cercano
// Math.floor(numero) trunca un número decimal y deja solo su parte entera (redondea para abajo)
// Math.fround(numero) devuelve el numero de presición flotante más cercano (4 bytes)
// Math.trunc(numero) trunca, deja solo la parte entera de un número decimal

// Y cuenta con los siguientes atributos:
 
// Math.PI; número pi
// Math.SQRT1_2; raíz cuadrada de 1/2
// Math.SQRT2; raíz cuadrada de 2
// Math.E; número e
// Math.LN2; logaritmo natural de 2
// Math.LN10; logaritmo natural de 10
// Math.LOG2E; logaritmo con base 2 de e
// Math.LOG10E; logaritmo con base 10 de e